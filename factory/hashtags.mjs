// 카테고리별 해시태그 (검색 유입용). 홈페이지 cards.ts 와 동일 계열.
const CATEGORY_TAGS = {
  사회: ["#사회이슈", "#뉴스", "#시사", "#오늘의뉴스", "#속보", "#뉴스요약"],
  경제: ["#경제", "#재테크", "#경제뉴스", "#주식", "#부동산", "#투자"],
  스포츠: ["#스포츠", "#스포츠뉴스", "#축구", "#야구", "#경기결과"],
  "IT·테크": ["#IT", "#테크", "#인공지능", "#AI", "#IT뉴스", "#신기술"],
  "문화·연예": ["#연예", "#문화", "#엔터", "#연예뉴스", "#드라마", "#영화"],
  트렌드: ["#트렌드", "#요즘", "#밈", "#핫이슈", "#급상승"],
};
const COMMON = ["#오즈백", "#ODDSBAG", "#오늘의이슈", "#이슈", "#카드뉴스", "#쇼츠", "#릴스"];

// n개 해시태그 문자열 (플랫폼별로 개수 조절: FB 8개, IG 30개 등)
export function hashtags(post, n = 10) {
  const pool = ["#오즈백", "#ODDSBAG", ...(CATEGORY_TAGS[post.category] || ["#이슈", "#뉴스"]), ...COMMON];
  return [...new Set(pool.filter((t) => t.length > 1))].slice(0, n).join(" ");
}
